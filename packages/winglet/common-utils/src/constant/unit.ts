/** 킬로(K) 단위 상수, 10^3 = 1,000 */
export const KILO = 10 ** 3;
/** 메가(M) 단위 상수, 10^6 = 1,000,000 */
export const MEGA = KILO * KILO;
/** 기가(G) 단위 상수, 10^9 = 1,000,000,000 */
export const GIGA = MEGA * KILO;
/** 테라(T) 단위 상수, 10^12 = 1,000,000,000,000 */
export const TERA = GIGA * KILO;
/** 페타(P) 단위 상수, 10^15 = 1,000,000,000,000,000 */
export const PETA = TERA * KILO;
/** 엑사(E) 단위 상수, 10^18 = 1,000,000,000,000,000,000 */
export const EXA = PETA * KILO;

/** 바이너리 킬로(KiB) 단위 상수, 2^10 = 1,024 */
export const KILO_2 = 2 ** 10;
/** 바이너리 메가(MiB) 단위 상수, 2^20 = 1,048,576 */
export const MEGA_2 = KILO_2 * KILO_2;
/** 바이너리 기가(GiB) 단위 상수, 2^30 = 1,073,741,824 */
export const GIGA_2 = MEGA_2 * KILO_2;
/** 바이너리 테라(TiB) 단위 상수, 2^40 = 1,099,511,627,776 */
export const TERA_2 = GIGA_2 * KILO_2;
/** 바이너리 페타(PiB) 단위 상수, 2^50 = 1,125,899,906,842,624 */
export const PETA_2 = TERA_2 * KILO_2;
/** 바이너리 엑사(EiB) 단위 상수, 2^60 = 1,152,921,504,606,846,976 */
export const EXA_2 = PETA_2 * KILO_2;
