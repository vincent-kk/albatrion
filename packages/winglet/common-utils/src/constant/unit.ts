/** Kilo (K) unit constant, 10^3 = 1,000 */
export const KILO = 10 ** 3;
/** Mega (M) unit constant, 10^6 = 1,000,000 */
export const MEGA = KILO * KILO;
/** Giga (G) unit constant, 10^9 = 1,000,000,000 */
export const GIGA = MEGA * KILO;
/** Tera (T) unit constant, 10^12 = 1,000,000,000,000 */
export const TERA = GIGA * KILO;
/** Peta (P) unit constant, 10^15 = 1,000,000,000,000,000 */
export const PETA = TERA * KILO;
/** Exa (E) unit constant, 10^18 = 1,000,000,000,000,000,000 */
export const EXA = PETA * KILO;

/** Binary kilo (KiB) unit constant, 2^10 = 1,024 */
export const KILO_2 = 2 ** 10;
/** Binary mega (MiB) unit constant, 2^20 = 1,048,576 */
export const MEGA_2 = KILO_2 * KILO_2;
/** Binary giga (GiB) unit constant, 2^30 = 1,073,741,824 */
export const GIGA_2 = MEGA_2 * KILO_2;
/** Binary tera (TiB) unit constant, 2^40 = 1,099,511,627,776 */
export const TERA_2 = GIGA_2 * KILO_2;
/** Binary peta (PiB) unit constant, 2^50 = 1,125,899,906,842,624 */
export const PETA_2 = TERA_2 * KILO_2;
/** Binary exa (EiB) unit constant, 2^60 = 1,152,921,504,606,846,976 */
export const EXA_2 = PETA_2 * KILO_2;
