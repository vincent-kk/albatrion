/** Millisecond (ms) unit constant, 1ms */
export const MILLISECOND = 1;
/** Second (s) unit constant, 1000ms */
export const SECOND = 1000 * MILLISECOND;
/** Minute (m) unit constant, 60s = 60,000ms */
export const MINUTE = 60 * SECOND;
/** Hour (h) unit constant, 60m = 3,600s = 3,600,000ms */
export const HOUR = 60 * MINUTE;
/** Day unit constant, 24h = 1,440m = 86,400s = 86,400,000ms */
export const DAY = 24 * HOUR;
